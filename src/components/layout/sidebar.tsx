export default function Sidebar() {
  const pathname = usePathname();
  const { data: user } = trpc.user.me.useQuery();

  const isActive = (href: string) => {
    if (href === "/dashboard") {
      return pathname === "/" || pathname === "/dashboard";
    }
    // Special handling for /admin to avoid matching /admin when on /admin/users etc.
    if (href === "/admin") {
      return pathname === "/admin";
    }
    return pathname.startsWith(href);
  };

  return (
    <aside className="flex h-screen w-64 flex-col border-r border-card-border bg-card">
      {/* Logo */}
      <div className="flex h-16 items-center gap-2 border-b border-card-border px-4">
        <div className="flex h-8 w-8 items-center justify-center rounded-lg bg-primary">
          <svg className="h-5 w-5 text-primary-foreground" fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M20 7l-8-4-8 4m16 0l-8 4m8-4v10l-8 4m0-10L4 7m8 4v10M4 7v10l8 4" />
          </svg>
        </div>
        <div>
          <h1 className="text-sm font-bold text-foreground">Policy Hub</h1>
          <p className="text-2xs text-muted">Kubernetes Policy Management</p>
        </div>
      </div>

      {/* Main Navigation */}
      <nav className="flex-1 space-y-1 px-2 py-4">
        {navigation.map((item) => (
          <Link
            key={item.name}
            href={item.href}
            className={cn(
              "flex items-center gap-3 rounded-md px-3 py-2 text-sm font-medium transition-colors",
              isActive(item.href)
                ? "bg-primary/10 text-primary border-l-2 border-primary"
                : "text-muted hover:bg-card-hover hover:text-foreground"
            )}
          >
            {icons[item.icon]}
            {item.name}
          </Link>
        ))}
      </nav>

      {/* Admin Navigation (Super Admins only) */}
      {user?.isSuperAdmin && (
        <div className="border-t border-card-border px-2 py-4">
          <div className="mb-2 px-3 text-xs font-semibold uppercase tracking-wider text-muted">
            Admin
          </div>
          {adminNavigation.map((item) => (
            <Link
              key={item.name}
              href={item.href}
              className={cn(
                "flex items-center gap-3 rounded-md px-3 py-2 text-sm font-medium transition-colors",
                isActive(item.href)
                  ? "bg-accent/10 text-accent"
                  : "text-muted hover:bg-card-hover hover:text-foreground"
              )}
            >
              {icons[item.icon]}
              {item.name}
            </Link>
          ))}
        </div>
      )}

      {/* Bottom Navigation */}
      <div className="border-t border-card-border px-2 py-4">
        {/* Theme Toggle */}
        <div className="mb-2 flex items-center justify-between px-3">
          <span className="text-xs font-medium text-muted">Theme</span>
          <ThemeSelect />
        </div>
        {bottomNavigation.map((item) => (
          <Link
            key={item.name}
            href={item.href}
            className={cn(
              "flex items-center gap-3 rounded-md px-3 py-2 text-sm font-medium transition-colors",
              isActive(item.href)
                ? "bg-primary/10 text-primary"
                : "text-muted hover:bg-card-hover hover:text-foreground"
            )}
          >
            {icons[item.icon]}
            {item.name}
          </Link>
        ))}
      </div>
    </aside>
  );
}
